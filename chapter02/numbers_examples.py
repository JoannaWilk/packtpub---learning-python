# numbers examples

a = 12
b = 3

print("a: ", a, ", b: ", b)
print('addition a + b: ', a + b)
print('subtraction b - a: ', b - a)
print('integer division a // b: ', a // b)
print('true division a / b: ', a / b)
print('multiplication a * b: ', a * b)
print('power b ** a: ', b ** a)
print('power 2 ** 1024: ', 2 ** 1024)
print("------------------")
print('true division a / b: ', 7 / 4)
print('integer division a // b: ', 7 // 4)
print('true division a / b: ', -7 / 4)
print('integer division a // b: ', -7 // 4)
print('truncation int(1.75):  ', int(1.75))
print('truncation int(-1.75):  ', int(-1.75))
print('modulo operator 10 % 3:  ', 10 % 3)
print('modulo operator 10 % 3:  ', 10 % 4)
