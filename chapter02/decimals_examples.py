# fractions examples

from decimal import Decimal as D


print("Decimal from float D(3.14): ", D(3.14))
print("Decimal from string D('3.14'): ", D('3.14'))
print('-----------------------')
print("Decimal from floats D(0.1) * D(3) - D(0.3): ", D(0.1) * D(3) - D(0.3))
print("Decimal from strings D('0.1') * D(3) - D('0.3'): ", D('0.1') * D(3) - D('0.3'))
