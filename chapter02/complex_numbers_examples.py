# complex numbers examples

c = 3.14 + 2.73j
d = 1 + 1j

print("c = 3.14 + 2.73j :", c)
print("real part c.real: ", c.real)
print("imaginary part c.imag: ", c.imag)
print("conjugate c.conjugate(): ", c.conjugate())
print("multiplication c * 2: ", c * 2)
print("power c ** 2: ", c ** 2)
print("addition d = 1 + 1j: ", d)
print("subtraction c - d: ", c - d)
